package Wrapperclassesdemo;

public class Demo1 {
    public static void main(String args[]){
        Integer i=new Integer(10);
        System.out.println(i);
        Integer i1=new Integer("20");
        System.out.println(i1);
        Short s=new Short( "1");
        System.out.println(s);
        short f=2;
        Short s1=new Short(f);
        System.out.println(s1);
        Character c=new Character('c');
        System.out.println(c);
        Float f1=new Float(10.5f);
        System.out.println(f1);
        Float f2=new Float(10.6);
        System.out.println(f2);
        Float f3=new Float("10.7");
        System.out.println(f3);
        Double d=new Double("10.4");
        System.out.println(d);
        Double d1=new Double(10.4);
        System.out.println(d1);
        Long l=new Long(100);
        System.out.println(l);
        Long l1=new Long("100");
        System.out.println(l1);
        byte b1=123;
        Byte b=new Byte(b1);
        System.out.println(b);
        Byte b2=new Byte("127");
        System.out.println(b2);
        Boolean b4=new Boolean(true);
        System.out.println(b4);
        Boolean b5=new Boolean(false);
        System.out.println(b4);
        Boolean b6=new Boolean("true");
        System.out.println(b6);
        Boolean b7=new Boolean("True");
        System.out.println(b7);
        Boolean b8=new Boolean("madhu");
        System.out.println(b8);
    }
}
